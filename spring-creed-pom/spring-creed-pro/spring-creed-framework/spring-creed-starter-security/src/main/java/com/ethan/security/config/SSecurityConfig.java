package com.ethan.security.config;

import com.ethan.security.core.service.PermissionApi;
import com.ethan.security.core.service.SecurityFrameworkService;
import com.ethan.security.core.service.SecurityFrameworkServiceImpl;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.security.config.annotation.method.configuration.EnableMethodSecurity;

@Configuration
@EnableMethodSecurity
public class SSecurityConfig {
    @Bean(name = "ss") // 使用 Spring Security 的缩写，方便使用
    public SecurityFrameworkService securityFrameworkService(PermissionApi permissionApi) {
        return new SecurityFrameworkServiceImpl(permissionApi);
    }
}
